def prepare(emoticons_list):
  emoticons = {}
  for l in emoticons_list:
    emoji = l[-1]
    l = l[:-1]
    emoticons.update({x:emoji for x in l})

  keys = [re.escape(k) for k in emoticons.keys()]
  em = '|'.join(keys)
  r = re.compile(f"({em})")
  return emoticons, r

